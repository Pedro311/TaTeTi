    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        /*
         * Recojemos los datos de los campos.
         */
        if (recojerDatos()) {
            if (rbMaquina.isSelected()) {
                JOptionPane.showMessageDialog(rootPane, txtNombre1.getText() + " intenta ganarme!! Si acaso puedes... jaja!!", "La Maquina dice...", JOptionPane.INFORMATION_MESSAGE);
            }

            enviarModelo();
            /*
             * Cerramos esta ventana.
             */
            Limpiar();
            juego.limpiarTablero();
            juego.Habilitar_LimpiaTablero();

            dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

