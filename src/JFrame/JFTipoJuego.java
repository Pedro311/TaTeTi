    public boolean recojerDatos() {
        
        /*Nos fijamos que esten llenos los campos.*/
        if (this.txtNombre1.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Llene el nombre del jugador 1 por favor.","[X] Error:", JOptionPane.ERROR_MESSAGE);
            return false;
            }
        if (this.txtNombre2.getText().equals("") && this.rbPersona.isSelected()){
            JOptionPane.showMessageDialog(this,"Llene el nombre del jugador 2 por favor.","[X] Error:", JOptionPane.ERROR_MESSAGE);
            return false;
            }
         if (this.txtNombre1.getText().equals(this.txtNombre2.getText())) {
            JOptionPane.showMessageDialog(this, "Escriba nombres diferentes para los jugadores.", "[X] Error:", JOptionPane.ERROR_MESSAGE);
            return false;
         }
         
          /*Recojemos los valores.*/
        this.tipo_juego = (this.rbPersona.isSelected()) ? PERSONAvsPERSONA : PERSONAvsMAQUINA;
        this.nombre1 = this.txtNombre1.getText();
        this.nombre2 = this.txtNombre2.getText();

        return true;
    
    }
    public void enviarModelo() {
        juego.recojerModelo();
    }
    
